public class UsandoArray {
    public static void main(String[] args) {
        int[] number = {1, 2, 3, 4, 5}; //Vetor de inteiros
           for (int numbe : number){ // Nesse caso o contador tem o lado esquerdo e o lado direito e ambos são diferentes. 
              System.out.println(numbe); 
           }
    }
}
/* Lado esquerdo = Declara a variavel para armazenamento temporario do valor do elemento durante a repetição
 * Lado direito = o array que esta sendo percorrido
*/